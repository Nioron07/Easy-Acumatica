"""Service method invocation screen.

Covers the read/delete methods generated by ServiceFactory:

- ``get_list(options=None)`` - supports the full set of query options
- ``get_by_id(entity_id)`` - single record
- ``get_by_keys(**key_fields)`` - single record by arbitrary keys
- ``get_ad_hoc_schema()`` - metadata only, no params
- ``get_files(entity_id)`` - file attachments for one record
- ``delete_by_id(entity_id)`` - destructive, requires confirmation
- ``delete_by_keys(**key_fields)`` - destructive, requires confirmation

Only ``get_list`` shows the query-option inputs (the single-record fetches
and destructive calls don't use them here). Destructive methods require
pressing Execute twice - the first press shows a warning in the status bar.
The call runs on a worker thread; the result renders as pretty JSON.
"""
